    private void jtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtCantidadKeyTyped
        // TODO add your handling code here:
        char car = evt.getKeyChar();
        if (!Character.isDigit(car) && car !='.') {
            evt.consume();
        }
    }//GEN-LAST:event_jtCantidadKeyTyped

    private float cantidad(){
        return Float.parseFloat(jtCantidad.getText());
    }
    
    private String cantidadDolar(){
        return Float.toString(this.cantidad() / dolar);
    }
    
    private String cantidadEuro(){
        return Float.toString(this.cantidad() / euro);
    }

